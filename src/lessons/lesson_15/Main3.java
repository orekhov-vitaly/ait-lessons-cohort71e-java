package lessons.lesson_15;

public class Main3 {
    public static void main(String[] args) {
        int x = 13;
        int y = 3;

        System.out.println(x + y);

        int a = x / y;
        System.out.println(a);

        double b = 7.6;
        System.out.println(b);

        b = x / y;
        System.out.println(b);

        b = x / (double) y;
        System.out.println(b);
    }
}
